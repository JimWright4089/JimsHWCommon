#include "mySqlDBSingleton.h"

//----------------------------------------------------------------------------
//  Global and Static data
//----------------------------------------------------------------------------
std::shared_ptr<MySQLDB> MySQLDBSingleton::sInstance = nullptr;

//----------------------------------------------------------------------------
//  Purpose:
//   GetInstance
//
//  Notes:
//
//----------------------------------------------------------------------------
MySQLDB &MySQLDBSingleton::getInstance()
{
    if (sInstance == nullptr) 
    {
        sInstance = std::shared_ptr<MySQLDB>(new MySQLDB());
    }
    return *sInstance;
}
